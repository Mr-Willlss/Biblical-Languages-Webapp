function sourceSections(description) {
  const blocks = String(description || "").split(/\n\s*\n/).map((block) => block.trim()).filter(Boolean);
  const sections = [];
  let title = "Understand the main idea";
  let body = [];
  let length = 0;
  const flush = () => {
    if (!body.length) return;
    sections.push({
      title,
      body,
      remember: sections.length === 0 ? "Pause and explain this idea in your own words before moving on." : "Look for the repeated pattern, not just the individual example."
    });
    title = "Build the idea step by step";
    body = [];
    length = 0;
  };
  blocks.forEach((block) => {
    const lines = block.split("\n").map((line) => line.trim()).filter(Boolean);
    const headingLike = lines[0]?.length < 70 && (/^[A-Z0-9 &—:'?]+$/.test(lines[0]) || lines[0].endsWith(":"));
    if (headingLike) {
      flush();
      title = lines.shift().replace(/:$/, "");
    }
    const paragraph = lines.join("\n");
    if (!paragraph) return;
    if (length && length + paragraph.length > 1000) flush();
    body.push(paragraph);
    length += paragraph.length;
  });
  flush();
  return sections;
}

function vocabularySection(lesson) {
  const examples = lesson.vocabulary.slice(0, 6).map((word) => {
    const pronunciation = word.transliteration ? ` (${word.transliteration})` : "";
    return `${word.script}${pronunciation} means “${word.english}”.${word.example ? ` Example: ${word.example}` : ""}`;
  });
  return {
    title: "See the lesson inside real words",
    body: ["Vocabulary is easier to remember when you connect the written form, its sound, and its meaning. Read each word aloud, then cover the English and recall it.", ...examples],
    remember: "Choose two words and say what each one means without looking."
  };
}

function compactSections(sections, limit = 3) {
  const compacted = sections.map((section) => ({ ...section, body: [...section.body] }));
  while (compacted.length > limit) {
    let mergeAt = 0;
    let smallest = Infinity;
    for (let index = 0; index < compacted.length - 1; index += 1) {
      const size = compacted[index].body.join(" ").length + compacted[index + 1].body.join(" ").length;
      if (size < smallest) { smallest = size; mergeAt = index; }
    }
    const first = compacted[mergeAt];
    const second = compacted[mergeAt + 1];
    first.body.push(`${second.title}:`, ...second.body);
    first.remember = second.remember;
    compacted.splice(mergeAt + 1, 1);
  }
  return compacted;
}

function buildLessonGuide(lesson) {
  const coaching = lesson.lang === "greek" ? GREEK_COACHING[lesson.lesson] : null;
  if (coaching?.sections) return [...compactSections(coaching.sections), vocabularySection(lesson)];
  const opening = {
    title: "Start with the big idea",
    body: [
      coaching?.bigIdea || lesson.grammarNote || lesson.summary,
      coaching?.why || "This lesson gives you a map before the details. When you understand why a language pattern exists, the examples stop feeling like disconnected facts and become easier to remember.",
      "Do not rush to memorize every form. First understand what the pattern is doing, then use the examples to train your eyes."
    ],
    remember: "After reading, say the big idea aloud in one sentence."
  };
  return [opening, ...compactSections(sourceSections(lesson.description)), vocabularySection(lesson)];
}

export { buildLessonGuide };
